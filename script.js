function funcLog(){
	if(document.querySelector('.Log form').style.visibility == 'visible'){
		document.querySelector('.Log form').style.visibility = 'hidden';
	}else{
		document.querySelector('.Log form').style.visibility = 'visible';
	}
}


	

function funcReg(){
	if(document.querySelector('.Reg form').style.visibility == 'visible'){
		document.querySelector('.Reg form').style.visibility = 'hidden';
	}else{
		document.querySelector('.Reg form').style.visibility = 'visible';
	}
}